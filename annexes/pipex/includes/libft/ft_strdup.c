#include "libft.h"

char	*ft_strdup(const char *s1)
{
	char	*copy;
	int		l;
	int		i;

	i = 0;
	l = ft_strlen(s1) + 1;
	copy = malloc(l * sizeof(char));
	if (!copy)
		return (0);
	while (s1[i])
	{
		copy[i] = s1[i];
		i++;
	}
	copy[i] = 0;
	return (copy);
}
